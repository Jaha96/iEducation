                                <p>
                                <b>Identification</b><br>
                                Genghis Khan banded the Mongolian tribes together for the first time in 1206 and formed a unified state. The steppe empires and nomadic culture created by the ancient Mongols hold a unique place in world history, and modern Mongols are very proud of this particular heritage.Mongolia is a large landlocked country in Central Asia, and is bordered on the north by the Russian Federation and on the south by the People's Republic of China. Measuring 604,100 square miles (1,565,000 square kilometers) in area. Mongolia's climate is extreme, with low precipitation and long harsh winters where temperatures can dip to -50 degrees centigrade. The capital city is Ulaanbaatar, meaning "Red Hero."With only 3 million people as of July 2016.</p>

                                <p>
                                <b>Food Customs at Ceremonial Occasions.</b><br>
                                Food is an important element of the Mongolian hospitality tradition. When guests arrive, each household sets out a special hospitality bowl containing homemade cheeses, flour pastries (bordzig), sugar cubes and candy. The fattest animals are slaughtered to be eaten. Meat-filled dumplings are traditionally served to guests. Vodka shots are served at regular intervals during a celebration.
                                </p>

                                <blockquote>
                                <p><b>Climate</b><br>
                                Spring comes after a severe winter, days become longer, and nights shorter. It is the time for snow to melt and for animals to come out from hibernation. Spring is the prosperous season of the year when everybody is calm and relaxed; grass turns green, anemones grow up and nature is covered with its green dress. Beginning in March spring usually lasts about 60 days although it can be as long as 70 days or as short as 45 days in some areas of the country</p></blockquote>

                                <p>Summer is the warmest season in Mongolia. Generally, precipitation is higher in summer than any time of the year. Rivers and streams are at their fullest in summer.It is the most pleasant time with abundant dairy products and there are many feasts and holidays of happy people. In Mongolia, summer lasts about 110 days from the end of May until September.</p>

                                <p>Autumn in Mongolia is the season of transition from the hot and wet summer to the cold and dry winter. There is less rainfall in autumn. Gradually it gets cooler and vegetables and grains are harvested at this time. Pasture and forests become yellow. Autumn is an important season in Mongolia in order to prepare for winter; harvesting the crops, vegetables and fodder; getting ready their cattle barns and sheds; preparing firewood and warming up their homes and so on. Autumn lasts about 60 days from the beginning of September until the early November. In some years, there are many long and sunny days in autumn.</p>



                                <p>Winter is the most severe, the coldest and longest season. All rivers, lakes, streams and ponds freeze in winter. It snows throughout the country, but not heavily. After making all the necessary preparations for a long winter, herdsmen stay at their winter camps. Winter starts early in November and lasts about 110 days until March. Sometimes it snows in September and November, but the heavy snow usually occurs at the beginning of November.</p>

                                
